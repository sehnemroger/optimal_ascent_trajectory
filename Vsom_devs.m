function [Vsomr,Vsomrr]=Vsom_devs(r)
% Função para a velocidade do som em função da altitude (Modelo do Paulo
% Sérgio, Pg. 158.)
%
% Entradas: h [m] (altitude)
RE=6378145;
A0=340;
A1=-7.59813873;
A2=4.41739218e-1;
A3=-1.12391220e-2;
A4=1.70917283e-4;
A5=-1.59888891e-6;
A6=6.51961005e-9;

Vsomr=(1/1000).*A1+(1/500000000000000000).*(r+(-1).*RE).*( ...
  1000000000000.*A2+(r+(-1).*RE).*(1500000000.*A3+(r+(-1).*RE).*( ...
  2000000.*A4+(r+(-1).*RE).*(2500.*A5+3.*A6.*r+(-3).*A6.*RE))));

Vsomrr=(1/500000).*A2+(1/100000000000000000).*(r+(-1).*RE).*(600000000.* ...
  A3+(r+(-1).*RE).*(1200000.*A4+(r+(-1).*RE).*(2000.*A5+3.*A6.*r+( ...
  -3).*A6.*RE)));
end